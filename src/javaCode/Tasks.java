package javaCode;

public class Tasks {

    private String descPl;
    private String descGe;

    public Tasks(String descPl, String descGe) {
        this.descPl = descPl;
        this.descGe = descGe;
    }

    public String getDescPl() {
        return descPl;
    }

    public void setDescPl(String descPl) {
        this.descPl = descPl;
    }

    public String getDescGe() {
        return descGe;
    }

    public void setDescGe(String descGe) {
        this.descGe = descGe;
    }
}
